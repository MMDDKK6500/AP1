package repeticao_while1;

public class Repeticao_While1 {

    public static void main(String[] args) {
        
        int x = 0;
        
        while(true) {
            
            System.out.println(x);
            
            x++;
            
            if (x > 100)
                break;
            
        }
        
    }
    
}
